package KursUdemy.Sekcja2_zmienne;

public class TypyProste_wartościDomyślne {

    public static int wartośćDomyślnaInt;
    public static long wartośćDomyślnaLong;
    public static byte wartośćDomyślnaByte;
    public static short wartośćDomyślnaShort;
    public static char wartośćDomyślnaChar;
    public static float wartośćDomyślnaFloat;
    public static double wartośćDomyślnaDouble;
    public static boolean wartośćDomyślnaBoolean;

    public static void main(String[] args) {
        System.out.println("Ten program ma na celu wyświetlić na konsoli wartości domyślne dla poszczególnych typów prostych");
        System.out.println("Aby sprawdzić wartości domyślne musimy sprawić, aby były one publiczne");
        System.out.println("Domyślna dla typu byte wynosi " + wartośćDomyślnaByte);
        System.out.println("Domyślna dla typu short wynosi " + wartośćDomyślnaShort);
        System.out.println("Domyślna dla typu int wynosi " + wartośćDomyślnaInt);
        System.out.println("Domyślna dla typu long wynosi " + wartośćDomyślnaLong);
        System.out.println("Domyślna dla typu char wynosi " + wartośćDomyślnaChar);
        System.out.println("Domyślna dla typu float wynosi " + wartośćDomyślnaFloat);
        System.out.println("Domyślna dla typu double wynosi " + wartośćDomyślnaDouble);
        System.out.println("Domyślna dla typu boolean wynosi " + wartośćDomyślnaBoolean);
        System.out.println("Oto wszystkie wartości domyślne dla typów prostych");




    }




}
