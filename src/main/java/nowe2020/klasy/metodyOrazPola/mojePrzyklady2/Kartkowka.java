package nowe2020.klasy.metodyOrazPola.mojePrzyklady2;

public class Kartkowka {
    private int pierwszePytanie;
    private int drugiePytanie;
    private int trzeciePytanie;
    private int czwartePytanie;
    private int piatePytanie;

    public int getPierwszePytanie() {
        return pierwszePytanie;
    }

    public void setPierwszePytanie(int pierwszePytanie) {
        this.pierwszePytanie = pierwszePytanie;
    }

    public int getDrugiePytanie() {
        return drugiePytanie;
    }

    public void setDrugiePytanie(int drugiePytanie) {
        this.drugiePytanie = drugiePytanie;
    }

    public int getTrzeciePytanie() {
        return trzeciePytanie;
    }

    public void setTrzeciePytanie(int trzeciePytanie) {
        this.trzeciePytanie = trzeciePytanie;
    }

    public int getCzwartePytanie() {
        return czwartePytanie;
    }

    public void setCzwartePytanie(int czwartePytanie) {
        this.czwartePytanie = czwartePytanie;
    }

    public int getPiatePytanie() {
        return piatePytanie;
    }

    public void setPiatePytanie(int piatePytanie) {
        this.piatePytanie = piatePytanie;
    }
}
